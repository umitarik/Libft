#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*str;

	str = s;
	while (n > 0)
	{
		if (*str == (unsigned char) c)
			return ((void *)str);
		n--;
		str++;
	}
	return (NULL);
}
#include <stdio.h>
int main()
{
	char s[10] = "hello";
	printf("%s",ft_memchr(s,'e',2));
}
